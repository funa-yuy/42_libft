#include "libft.h"
#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ptr;
	size_t			i;

	ptr = b;
	i = 0;
	while (len > i)
	{
		ptr[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
