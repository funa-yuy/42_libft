#include "libft.h"
#include <stddef.h>

size_t	ft_strlcpy(char *restrict dst, const char *restrict src, \
size_t dstsize)
{
	unsigned int	i;

	i = 0;
	if (dstsize != 0)
	{
		while (src[i] && i < dstsize - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	while (src[i] != '\0')
		i++;
	return (i);
}
